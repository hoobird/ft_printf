#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*temp;
	size_t			count;

	count = 0;
	temp = (unsigned char *) s; 
	while (count < n)
	{
		*temp = c;
		count++;
		temp++;
	}
	return (s);
}
